package sample;
public class TableContent {
    private int ki;
    private int yi;
    public TableContent(int ki, int yi) {
        this.ki = ki;
        this.yi = yi;
    }
    public TableContent() { }
    public int getKi() { return ki; }
    public void setKi(int ki) { this.ki = ki; }
    public int getYi() { return yi; }
    public void setYi(int yi) {
        this.yi = yi;
    }
}
